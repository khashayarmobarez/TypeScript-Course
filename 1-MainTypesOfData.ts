// main types of data:
// 1- string
// 2- number (integer)
// 3- boolean

// string example
let BlogTitle : string = 'string example';
console.log(typeof BlogTitle)

// number example
let BlogLikesCount : number = 5
console.log(typeof BlogLikesCount)

// boolean example
let isPublished: boolean = true;
console.log(typeof isPublished)

// you can set the type of a variable with type of another variable
let user : typeof BlogTitle
user = 'khashy'
console.log("user type:",typeof user)

